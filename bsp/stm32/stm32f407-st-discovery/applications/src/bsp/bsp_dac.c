/*****************************************************************************************
** 函数名称：lvps1的DA输出
** 函数描述：
** 输入参数：
** 返回值  ：无
******************************************************************************************/
void bsp_Lvps1_dac_out_set(uint32_t data)
{
	static uint8_t dac1_fristtime = 0;
	
	basic_dac_set_value(DAC_CHANNEL_1, DAC_ALIGN_12B_R, data);
	
	if( dac1_fristtime == 0)
	{	
		basic_dac_start(DAC_CHANNEL_1);
		dac1_fristtime = 1;
	}
}
